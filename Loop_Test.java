public class Loop_Test {

	public static void main(String[] args) {
		/*
		 * while loop 
		 * do while loop
		 * for loop
		 * 
		 */
		
		//For printing 1 to 10 
		for(int i=1;i<11;i++)
		{
			System.out.println(i);
		}
		
		int j=1;
		while(j<11)
		{
			System.out.println(j);
			j++;
		}
		 int k=1;
		 do {
				
				System.out.println(k);
				k++;
			}
			while (k<11);
		
	}
		
	

}
